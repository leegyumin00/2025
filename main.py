import streamlit as st
import random

# Streamlit 앱의 타이틀을 설정해요
st.title('🍀 오늘의 운세 🍀')

# 운세 메시지 리스트를 준비해요
fortunes = [
    "오늘은 당신에게 행운이 가득한 하루가 될 거예요!✨",
    "예상치 못한 좋은 소식이 찾아올 수 있어요. 귀를 기울여보세요!👂",
    "새로운 시작에 좋은 날이에요. 망설이지 말고 도전해보세요!🚀",
    "작은 어려움이 찾아올 수 있지만, 지혜롭게 헤쳐나갈 수 있을 거예요.💡",
    "주변 사람들과의 관계에서 행복을 찾을 수 있는 하루예요.💖",
    "노력한 만큼 결실을 맺는 날이니, 긍정적인 마음을 가지세요!💪",
    "뜻밖의 수입이 생길 수도 있겠어요.🤑",
    "잠시 쉬어가며 자신을 돌아보는 시간이 필요해요.🧘‍♀️",
    "창의적인 아이디어가 샘솟는 날이에요. 메모해두세요!✍️",
    "새로운 배움의 기회가 찾아올 수 있으니 놓치지 마세요.📚"
]

# 운세 보기 버튼을 만들어요
if st.button('오늘의 운세 보기!'):
    # 버튼이 눌리면 fortunes 리스트에서 무작위로 하나의 운세를 선택해요
    today_fortune = random.choice(fortunes)
    
    # 선택된 운세를 화면에 표시해요
    st.success(f"두근두근! 당신의 오늘의 운세는 바로... **{today_fortune}**")
    st.balloons() # 운세를 볼 때 풍선 효과도 넣어주면 더 재미있겠죠?
else:
    # 아직 버튼을 누르지 않았을 때 보여줄 메시지예요
    st.info('버튼을 눌러서 오늘의 운세를 확인해보세요!🔮')
